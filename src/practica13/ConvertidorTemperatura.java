    private void btnConvertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvertirActionPerformed
        // TODO add your handling code here:
        
        try{
        String celsiusDato = txtCelsius.getText().trim();
        
        
        if(celsiusDato.isEmpty()){
            JOptionPane.showMessageDialog(this, "Debes ingresar datos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int conversion = Integer.parseInt(celsiusDato);
        int gradosF = (conversion * 9/5) + 32;
        JOptionPane.showMessageDialog(this, "Grados Fahrenheit: " + gradosF, "Exito", JOptionPane.INFORMATION_MESSAGE);
        
        } catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(this, "Los grados deben ser n√∫meros", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnConvertirActionPerformed

